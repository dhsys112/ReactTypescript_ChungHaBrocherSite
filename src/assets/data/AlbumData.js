export const AlbumsLength = AlbumIntroDatas.length
// Assign common ids to datas
export const MakeCommonAlbumIds = (albumLengths, AlbumIntroDataArr, SingleAlbumArr) => {
    console.log("make Ids")
    const AlbumIds = []
    Array(albumLengths).fill(0).forEach(e => AlbumIds.push(nanoid()))
    AlbumIds.forEach((AlbumId,idx) => {
        // console.log("albumId in making func", id )
        AlbumIntroDataArr[idx].id     = AlbumId
        SingleAlbumArr[idx].id        = AlbumId

        AlbumIntroDataArr[idx].order  = AlbumId
        SingleAlbumArr[idx].order     = AlbumId
    })
}
